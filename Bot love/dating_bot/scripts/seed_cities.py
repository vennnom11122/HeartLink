async def main() -> None:
    settings = get_settings()
    session_factory = make_session_factory(settings)
    async with session_scope(session_factory) as session:
        for name, region, population in CITY_ROWS:
            city = await session.scalar(select(City).where(City.name == name, City.region == region))
            if city is None:
                session.add(City(name=name, region=region, population=population, is_active=True))
            else:
                city.population = population
                city.is_active = True
        print(f"Seeded {len(CITY_ROWS)} cities with population >= 300,000")


if __name__ == "__main__":
    asyncio.run(main())
